import random
from typing import Iterable

from src.domain.task import Task


class TaskGenerator:
    """Генератор случайных задач"""

    _id = 1
    TEXTS = [
        "Исправить баг с гусем, который съел дедлайн",
        "Написать тесты для гусиного дозора",
        "Добавить функционал кормления гусей в прод",
        "Починить гуся, украл ноутбук у разработчика",
        "Разблокировать вход в офис от стаи гусей",
        "Переписать говнокод, который написал гусь",
        "Освободить серверную от захвативших ее гусей",
        "Провести ревью кода, пока гусь не ущипнул",
        "Задеплоить фикс после гусиного переполоха",
        "Восстановить документацию, съеденную гусем",
        "Пофиксить гуся в продакшене срочно",
        "Учесть замечания гусей на код-ревью",
        "Обсудить с гусем переход на Go",
        "Выполнить задачи от гуся-тимлида",
        "Разобрать баги, которые поделили гуси",
    ]

    def __init__(self, count: int) -> None:
        """Инициализация с количеством задач"""
        self.count = count

    def get_tasks(self) -> Iterable[Task]:
        """Генерирует указанное количество задач"""
        for _ in range(self.count):
            yield Task(
                TaskGenerator._id,
                random.choice(TaskGenerator.TEXTS),
                random.randint(1, 5),
            )
            TaskGenerator._id += 1
